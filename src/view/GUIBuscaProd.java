public void buscarProd() throws SQLException{
        try{
            ManipulaProduto mp = new ManipulaProduto();
            ArrayList<Produto> alP = new ArrayList();
            alP = mp.buscarProd();
            for(int i=0; i<alP.size(); i++){
                dtm.addRow(new String[]{
                String.valueOf(alP.get(i).getCodProd()),    
                String.valueOf(alP.get(i).getNomeProd()),
                String.valueOf(alP.get(i).getQuantidade()),
                String.valueOf(alP.get(i).getValorDeVenda())
                
            });
            
            }//Fecha for
            
        
            tblProd.setModel(dtm);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, 
                    "Erro na tela de busca \n"+e,
                    "ERRO" ,
                    JOptionPane.ERROR_MESSAGE);
        }//fecha catch
    }//fecha metodo buscar()

