public void filtrarProd(){
        try{
            if(txtBuscaProd.getText().isEmpty()){
                buscarProd();
            }else{
                ManipulaProduto mp = new ManipulaProduto();
                String pesquisa = cmbTipoProd.getSelectedItem().toString();
                String query = null;
                if(pesquisa.equals("Código do Produto")){
                    query = "where codprod like '%"+txtBuscaProd.getText()+"%'";
                }else if(pesquisa.equals("Nome do Produto")){
                    query = "where nomeprod like '%"+txtBuscaProd.getText()+"%'";
                    }else if(pesquisa.equals("Quantidade")){
                    query = "where quantidade like '%"+txtBuscaProd.getText()+"%'";
                    
                }else{
                    query = "where valordevenda like '%"+txtBuscaProd.getText()+"%'";
                }//fecha else
            ArrayList<Produto> alP = new ArrayList();
            alP = mp.filtrarProdutos(query);
            for (int i = 0; i<alP.size(); i++) {
               dtm.addRow(new String[]{
                String.valueOf(alP.get(i).getCodProd()),alP.get(i).getNomeProd()
            }); 
            }
              tblProd.setModel(dtm);
         
         }//fecha else
        
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, 
                    "Erro ao filtrar\n" +e,
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//Fecha o método filtrar
