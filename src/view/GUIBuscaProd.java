public void selecionarValorProd(){
       
        int linhaProd = tblProd.getSelectedRow();
        
       
        if(linhaProd == -1){
            JOptionPane.showMessageDialog(rootPane, 
                    "Nenhuma linha selecionada",
                    "AVISO!",
                    JOptionPane.WARNING_MESSAGE);
        }else{
           
            String codProd = (String)tblProd.getValueAt(linhaProd, 0);
            String nomeProd = (String)tblProd.getValueAt(linhaProd, 1);
            String quantidade = (String)tblProd.getValueAt(linhaProd, 2);
            String valorDeVenda = (String)tblProd.getValueAt(linhaProd, 3);
            
            txtNomeProdAlterar.setText(nomeProd);
        }
    }//Fecha o método selecionarValor()

public void excluir(){
        try{
          int linhaProd = tblProd.getSelectedRow();
        
       
        if(linhaProd == -1){
            JOptionPane.showMessageDialog(rootPane, 
                    "Nenhuma linha selecionada",
                    "AVISO!",
                    JOptionPane.WARNING_MESSAGE);
        }else{
            ManipulaProduto mp = new ManipulaProduto();
            String codProd = (String)tblProd.getValueAt(linhaProd, 0);
            
           
           int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclusão", JOptionPane.YES_NO_OPTION);
           if(resposta == JOptionPane.YES_OPTION){
                mp.excluir(Integer.parseInt(codProd));
               JOptionPane.showMessageDialog(rootPane, 
                   "Excluído com Sucesso",
                   "EXCLUSÃO",
                   JOptionPane.INFORMATION_MESSAGE);
           }else if(resposta == JOptionPane.NO_OPTION) {
                  JOptionPane.showMessageDialog(rootPane, 
                   "Exclusão cancelada",
                   "EXCLUSÃO",
                   JOptionPane.INFORMATION_MESSAGE);
               
           }
           
        
        
        }//fecha else
                
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(rootPane,
                            "Erro ao excluir \n"+e,
                            "ERRO",
                            JOptionPane.ERROR_MESSAGE);
                }
    }//Fecha método excluir

