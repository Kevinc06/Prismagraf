public void salvar2(){
        Produto p = new Produto();
        p.setCodProd(Integer.parseInt(txtCodProd2.getText()));
        p.setNomeProd(txtNomeProd2.getText());
        p.setQuantidade(Integer.parseInt(txtQuant2.getText()));
        p.setValorDeVenda(txtValorVend2.getText());
        
        ManipulaProduto mp = new ManipulaProduto();
      try{
          mp.cadastrarProd(p);
          JOptionPane.showMessageDialog(rootPane,
                  "Produto Cadastrado",
                  "Cadastrado",
                  JOptionPane.INFORMATION_MESSAGE);
      }catch(SQLException e){
          JOptionPane.showMessageDialog(rootPane, 
                  "Erro ao cadastrar \n"+e,
                  "ERRO",
                  JOptionPane.ERROR_MESSAGE);
          System.out.println("ERRO");
      }
    }

